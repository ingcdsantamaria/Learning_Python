# siempre que trabajemos con numeros podemos hacer uso de operadores aritmeticos como:
#  Suma
sum = 3 + 2
print("suma: ",sum)
#  Resta
rest = 3 - 2
print("resta: ",rest)
#  Multiplicacion
mult = 3 * 2
print("multiplicacion: ",mult)
#  Division
div = 3 / 2
print("division: ",div)
#  Modulo
mod = 3 % 2
print("modulo: ",mod)
#  Exponente
exp = 3 ** 2
print("exponente: ",exp)
#  Division entera
div_int = 3 // 2
print("division entera: ",div_int)
