# in y not in son operadores de pertenencia, in evalua si un elemento pertenece a una lista, tupla, diccionario o cadena de texto, not in evalua si un elemento no pertenece a una lista, tupla, diccionario o cadena de texto

randomListNumbers = [1,4,58,3,8,67,3,8]

#Ejemplo con in este nos retornara un valor booleano in lo que hace es preguntar si un elemento pertenece a una lista, tupla, diccionario o cadena de texto

print(1 in randomListNumbers)
print(2 in randomListNumbers)

#Ejemplo con not in este nos retornara un valor booleano not in lo que hace es preguntar si un elemento no pertenece a una lista, tupla, diccionario o cadena de texto

print(1 not in randomListNumbers)
print(2 not in randomListNumbers)